{ 
	Uint16 K;
    char temp;
	WriteIns (0x0c0,flag);	//显示起始行设置(0行)
   	WriteIns (page,flag);	//设置页
    n=n*32;
    for(K=0;K<16;K++,Yaddress++) 
	    	{
	        	temp=table1[K+n];	//取数据
	        	WriteData (temp,Yaddress,flag);//写数据
	        }
		Yaddress=Yaddress-16;//下半字首列地址
	    page=page+1;	//每页8行,字对应的点阵为：宽x高=16x16 
	    WriteIns (page,flag);//设置下半字页
	    for(;K<32;K++,Yaddress++)
	      	{
	       		temp=table1[K+n];//取数据
	       		WriteData (temp,Yaddress,flag);//写数据
	      	}	
}

